package day03.com.ict.edu;

public class Ex06_oper {
	public static void main(String[] args) {
		/*	대입연산 : =, +=, -=, *=, /=, %=
		 * 	A += B => A = A + B
		 * 	A -= B => A = A - B
		 * 	A *= B -> A = A * B 
		 * 	...and so forth
		 * 
		 * */
		int su1 = 7;
		
		su1 += 4 ;
		System.out.println(su1);
		
		su1 -= 4 ;
		System.out.println(su1);
		
		
	}
}
